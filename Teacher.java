public class Teacher {
    private String name; 
    private int hourlySalary;

    public void setName(String name) {
        this.name = name; 
    }

    public void setHourlySalary(int hourlySalary) {
        this.hourlySalary = hourlySalary;
    }
    
    public String getName() {
        return name; 
    }

    public int getHourlySalary() {
        return hourlySalary;
    }
}



